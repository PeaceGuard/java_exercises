public class sum_100 {
    public static void main(String[] args) {
        Int tot = 0;
        for (Int num = 1; num <= 100; num++) {
            tot = tot + num;
            System.out.println(tot);
        for (String arg : args) {
            System.out.println(arg);
        }
    }
    }
}

